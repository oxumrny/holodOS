export function detectCategory(name: string): ProductCategory {
  const normalized = name.trim().toLowerCase();

  for (const rule of RULES) {
    if (rule.keywords.some((keyword) => normalized.includes(keyword))) {
      return rule.category;
    }
  }

  return 'прочее';
}
